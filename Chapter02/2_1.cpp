/*Write a program that stores the integers 50 and 100 in variables
and stores the sum of these two in a variable named total*/

#include <iostream>
using namespace std;

int main(void)
{
    //initialize x
    int x = 50;
    //initialize y
    int y = 100;
    //define total
    int total = x + y;

    //output total
    cout<<total<<endl;
    return 0;
}